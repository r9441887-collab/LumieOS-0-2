pub const fn make_color(r: u8, g: u8, b: u8) -> u32 {
    r as u32 | (g as u32) << 8 | (b as u32) << 16
}

pub unsafe fn draw_char(fb: *mut u32, pitch_px: u32, x: u32, y: u32, fg: u32, bg: u32, c: u8) {
    if c < 32 || c > 126 {
        return;
    }
    let idx = (c - 32) as usize;
    for row in 0usize..16 {
        let bits = FONT_8X16[idx][row];
        let offset = ((y as u64 + row as u64) * pitch_px as u64 + x as u64) as usize;
        let line = fb.add(offset);
        if bits == 0xFF {
            for col in 0..8 {
                ptr::write_volatile(line.add(col), fg);
            }
        } else if bits == 0x00 {
            for col in 0..8 {
                ptr::write_volatile(line.add(col), bg);
            }
        } else {
            for col in 0..8 {
                let color = if bits & (0x80 >> col) != 0 { fg } else { bg };
                ptr::write_volatile(line.add(col), color);
            }
        }
    }
}

pub unsafe fn draw_string(fb: *mut u32, pitch_px: u32, mut x: u32, y: u32, fg: u32, bg: u32, s: &str) {
    for &byte in s.as_bytes() {
        draw_char(fb, pitch_px, x, y, fg, bg, byte);
        x += 8;
    }
}

pub unsafe fn fill_rect(fb: *mut u32, pitch_px: u32, x: u32, y: u32, w: u32, h: u32, color: u32) {
    for j in 0..h {
        let offset = ((y as u64 + j as u64) * pitch_px as u64 + x as u64) as usize;
        let line = fb.add(offset);
        for i in 0..w {
            ptr::write_volatile(line.add(i as usize), color);
        }
    }
}

pub unsafe fn rounded_rect(fb: *mut u32, pitch_px: u32, x: i32, y: i32, w: i32, h: i32, r: i32, color: u32) {
    fill_rect(fb, pitch_px, (x + r) as u32, y as u32, (w - r * 2) as u32, h as u32, color);
    fill_rect(fb, pitch_px, x as u32, (y + r) as u32, w as u32, (h - r * 2) as u32, color);
    for i in 0..r {
        let len = r - i;
        fill_rect(fb, pitch_px, (x + i) as u32, (y + len) as u32, 1, 1, color);
        fill_rect(fb, pitch_px, (x + w - 1 - i) as u32, (y + len) as u32, 1, 1, color);
        fill_rect(fb, pitch_px, (x + i) as u32, (y + h - 1 - len) as u32, 1, 1, color);
        fill_rect(fb, pitch_px, (x + w - 1 - i) as u32, (y + h - 1 - len) as u32, 1, 1, color);
    }
}
